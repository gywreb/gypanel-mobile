import React, { useState } from "react";
import { StyleSheet } from "react-native";

import { View, Text } from "react-native";

import { appColor } from "../configs/styles";

import AppActiveRoleButton from "./AppActiveRoleButton";

const AppRoleItem = ({
  name,
  isActive,
  methods,
  permissions,
  id,
  handleActive,
  customContainer,
  activeStyles,
  dotStyles,
  dot,
}) => {
  const [loading, setLoading] = useState(false);

  const onPress = async () => {
    setLoading(true);
    await handleActive(id);
    setLoading(false);
  };

  return (
    <View style={[styles.container, customContainer]}>
      <View style={styles.box}>
        <View style={{ flexDirection: "row", justifyContent: "space-between" }}>
          {name && <Text style={styles.name}>{name.toUpperCase()}</Text>}
          <View>
            <View
              style={{
                flexDirection: "row",
                alignItems: "center",
              }}
            >
              {dot && (
                <View
                  style={[
                    {
                      width: 10,
                      height: 10,
                      borderRadius: 5,
                      backgroundColor: isActive
                        ? appColor.active
                        : appColor.inActive,
                      position: "absolute",
                    },
                    dotStyles,
                  ]}
                ></View>
              )}
            </View>
          </View>
        </View>
        {permissions && (
          <View style={[styles.subList]}>
            <Text style={styles.subTitle}>Permissions: </Text>
            <View style={{ flexDirection: "row" }}>
              {permissions.map((permission, index) => (
                <View key={index} style={[styles.subItemBox]}>
                  <Text style={[styles.subItemText]}>{permission}</Text>
                </View>
              ))}
            </View>
          </View>
        )}
        {methods && (
          <View style={[styles.subList]}>
            <Text style={styles.subTitle}>Methods: </Text>
            <View style={{ flexDirection: "row" }}>
              {methods.map((method, index) => (
                <View key={index} style={[styles.subItemBox]}>
                  <Text style={[styles.subItemText]}>{method}</Text>
                </View>
              ))}
            </View>
          </View>
        )}
      </View>
      <View
        style={[
          styles.isActiveCollar,
          isActive ? styles.Active : styles.inActive,
        ]}
      ></View>
      <AppActiveRoleButton
        isActive={isActive}
        loading={loading}
        onPress={onPress}
        positionStyles={activeStyles}
      />
    </View>
  );
};

export default AppRoleItem;

const styles = StyleSheet.create({
  container: {
    position: "relative",
    width: "100%",
    height: 120,
    marginBottom: 18,
    backgroundColor: appColor.white,
    borderRadius: 10,
    shadowColor: "#000",
    shadowOffset: {
      width: 0,
      height: 6,
    },
    shadowOpacity: 0.37,
    shadowRadius: 7.49,

    elevation: 12,
  },
  box: {
    height: "100%",
    paddingHorizontal: 15,
    paddingVertical: 5,
    justifyContent: "space-around",
    borderRadius: 8,
  },
  name: {
    fontSize: 15,
    fontWeight: "700",
  },
  isActiveCollar: {
    position: "absolute",
    height: "100%",
    width: 7,
    top: 0,
    left: 0,
    borderTopLeftRadius: 8,
    borderBottomLeftRadius: 8,
  },
  Active: {
    backgroundColor: appColor.active,
  },
  inActive: {
    backgroundColor: appColor.inActive,
  },
  subList: {
    flexDirection: "row",
    alignItems: "center",
    flexWrap: "wrap",
  },
  subItemBox: {
    backgroundColor: appColor.gray4,
    paddingVertical: 4,
    paddingHorizontal: 8,
    borderRadius: 20,
    marginHorizontal: 3,
  },
  subItemText: {
    fontWeight: "bold",
    color: "#696969",
  },
  subTitle: {
    fontWeight: "bold",
  },
});
